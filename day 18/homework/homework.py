# 4) For loop-ის მუშაობის ახსნა სახატავი აპლიკაციის მაგალითით  
# წარმოიდგინე რომ სახატავი პროგრამით (მაგალითად Paint-ით) ხაზავ წრეებს  
# და გინდა დახატო 10 წრე სხვადასხვა ადგილა  
# თუ ხელით დახატავ დრო დაგეხარჯება მაგრამ For loop-ის გამოყენებით შეგიძლია ავტომატურად დახატო ყველა წრე

import turtle  # Python-ის ჩაშენებული ბიბლიოთეკა გრაფიკისთვის  

turtle.speed(5)  # ვაყენებთ ხატვის სისწრაფეს  

for i in range(10):  # 10 წრის დახატვა  
    turtle.penup()  
    turtle.goto(i * 30, 0)  # ყოველი წრე 30 პიქსელით გვერდზე დაიხატოს  
    turtle.pendown()  
    turtle.circle(20)  # ვხატავთ წრეს რადიუსით 20  

turtle.done()  # ხატვის დასრულება  
