# Examples of "<" (less than)
print(3 < 5)     # True
print(7 < 2)     # False
print(-1 < 0)    # True

# Examples of ">" (greater than)
print(5 > 3)     # True
print(2 > 7)     # False
print(0 > -1)    # True

# Examples of "==" (equal to)
print(5 == 5)    # True
print(3 == 4)    # False
print(0 == 0)    # True

# Examples of "!=" (not equal to)
print(5 != 3)    # True
print(4 != 4)    # False
print(0 != -1)   # True






# == (ტოლია)
print("Hello" == "Hello")           # True
print("Hello" == "HelloWorld")      # False

# != (არ არის ტოლი)
print("Python" != "python")         # True
print("ChatGPT" != "ChatGPT")       # False

# > (დიდია)
print("apple" > "banana")           # False (ანბანურობის მიხედვით)
print("zebra" > "antelope")         # True

# < (ნაკლებია)
print("Alpha" < "Beta")             # True
print("Gamma" < "alpha")            # True (სტრინგი ანბანურობასა და რეგისტრის მიხედვით იშლება)

# სხვადასხვა სიგრძის სტრინგების შედარება
print("Hi" > "Hello")               # False
print("Code" < "Coding")            # True






