def main():
    print("­Ъћј рЃарЃўрЃфрЃ«рЃЋрЃўрЃА рЃерЃћрЃЏрЃЮрЃгрЃЏрЃћрЃЉрЃљ рЃЮрЃа рЃърЃўрЃарЃЮрЃЉрЃљрЃќрЃћ")
    number = int(input("7"))  
    
    # рЃърЃўрЃарЃЮрЃЉрЃљ 1: рЃарЃўрЃфрЃ«рЃЋрЃў рЃБрЃюрЃЊрЃљ рЃўрЃДрЃЮрЃА 10-рЃќрЃћ рЃЏрЃћрЃбрЃў
    condition1 = number > 10
    
    # рЃърЃўрЃарЃЮрЃЉрЃљ 2: рЃарЃўрЃфрЃ«рЃЋрЃў рЃБрЃюрЃЊрЃљ рЃўрЃДрЃЮрЃА рЃџрЃБрЃгрЃў
    condition2 = number % 2 == 0
    
    if condition1 and condition2:
        print(f"РюЁ рЃарЃўрЃфрЃ«рЃЋрЃў {number} 10-рЃќрЃћ рЃЏрЃћрЃбрЃўрЃф рЃљрЃарЃўрЃА рЃЊрЃљ рЃџрЃБрЃгрЃўрЃф.")
    elif condition1 or number >= 0:
        print(f"Рё╣№ИЈ рЃарЃўрЃфрЃ«рЃЋрЃў {number} рЃљрЃю 10-рЃќрЃћ рЃЏрЃћрЃбрЃўрЃљ, рЃљрЃю рЃЊрЃљрЃЊрЃћрЃЉрЃўрЃЌрЃў (рЃЏрЃўрЃюрЃўрЃЏрЃБрЃЏ 0).")
    else:
        print(f"РЮї рЃарЃўрЃфрЃ«рЃЋрЃў {number} рЃљрЃарЃф 10-рЃќрЃћ рЃЏрЃћрЃбрЃўрЃљ рЃЊрЃљ рЃљрЃарЃф рЃЊрЃљрЃЊрЃћрЃЉрЃўрЃЌрЃў.")

if __name__ == "__main__":
    main()



