# ორი რიცხვის შეყვანა
num1 = float(input("77"))
num2 = float(input("69"))

# მათემატიკური ოპერაციები
sum_result = num1 + num2
diff_result = num1 - num2
prod_result = num1 * num2
div_result = num1 / num2 if num2 != 0 else "განაყოფი არ შეიძლება ნულით"

# შედეგების ბეჭდვა
print(f"ჯამი: {sum_result}")
print(f"სხვაობა: {diff_result}")
print(f"ნამრავლი: {prod_result}")
print(f"განაყოფი: {div_result}")


# კონკატენაცია (Concatenation) ნიშნავს ორი ან მეტი სტრინგის ერთმანეთზე მიერთებას.
# Python-ში ეს ხორციელდება "+" ოპერატორის გამოყენებით


str1 = "გამარჯობა, "
str2 = "როგორ ხარ?"
result = str1 + str2  # აქ ხდება ორი სტრინგის მიერთება
print(result)  # შედეგად მივიღებთ: "გამარჯობა, როგორ ხარ?"

# კონკატენაცია იყენებს მხოლოდ სტრინგებს, მაგრამ თუ სხვა ტიპის მონაცემებს გინდათ მიერთება
# საჭიროა მათი სტრინგად გარდაქმნა str() ფუნქციით

# int() ფუნქცია
# ეს ფუნქცია იღებს მონაცემებს და გარდაქმნის მთელ რიცხვად (integer).
# მაგალითად, თუ გინდათ სტრინგი "123" ან ათწილადი რიცხვი 123.45 გადაიყვანოთ მთელ რიცხვად,
# int() ფუნქცია დაიგნორებს ათწილადის ნაწილს და დატოვებს მხოლოდ მთელ რიცხვს.

example1 = "123"  # სტრინგი
result1 = int(example1)  # გადაიქცევა მთელ რიცხვად 123
example2 = 123.45  # ათწილადი რიცხვი
result2 = int(example2)  # გადაიქცევა 123-ად (მხოლოდ მთელ ნაწილს ინახავს)

# float() ფუნქცია
# ეს ფუნქცია იღებს მონაცემებს და გარდაქმნის ათწილად რიცხვად (floating-point number)
# გამოიყენება, როდესაც გჭირდებათ რიცხვი, რომელიც შეიცავს წერტილს (დეკიმალს).
# მაგალითად, "123.45" გადაიქცევა ათწილად რიცხვად 123.45.

example3 = "123.45"  # სტრინგი
result3 = float(example3)  # გადაიქცევა 123.45-ად
example4 = 123  # მთელი რიცხვი
result4 = float(example4)  # გადაიქცევა 123.0-ად მთელს მიემატება დეკიმალური ნაწილი
